        if (!result) return;

        const summary = `
rAsh Score Report for ${result.brand}
=========================================
rAsh Score: ${result.score}/100

Breakdown:
- Recommendation: ${result.breakdown.recommendation}/40
- Sentiment: ${result.breakdown.sentiment}/30
- Prominence: ${result.breakdown.prominence}/20
- Accuracy: ${result.breakdown.accuracy}/10

Tips:
${result.tips.map((tip, i) => `${i + 1}. ${tip}`).join("\n")}

Generated by rAsh Score (https://rashscore.live)
        `.trim();

        try {
            await navigator.clipboard.writeText(summary);
            addToast("Results copied to clipboard!", "success");
        } catch {
            addToast("Failed to copy results", "error");
        }
